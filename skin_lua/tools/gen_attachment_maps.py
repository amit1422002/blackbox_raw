def is_attach_base(k: int) -> bool:
    if 200_000 <= k < 300_000:
        return True
    if 291_000 <= k < 292_000:
        return True
    return False

def main():
    skin_id = None
    maps = {}
    for line in RAW.strip().splitlines():
        line = line.strip()
        if not line:
            continue
        parts = line.split()
        if len(parts) == 2 and parts[1].isdigit() and int(parts[1]) >= 1100000000:
            skin_id = int(parts[1])
            maps.setdefault(skin_id, {})
            continue
        if len(parts) == 2 and skin_id and parts[0].isdigit():
            k, v = int(parts[0]), int(parts[1])
            if is_attach_base(k):
                maps[skin_id][k] = v

    out = Path(__file__).resolve().parents[1] / 'app' / 'src' / 'main' / 'assets' / 'skin_attachment_maps.lua'
    lines = ['-- Auto-generated attachment base -> skin attachment overrides', 'return {']
    for sid in sorted(maps.keys()):
        if not maps[sid]:
            continue
        lines.append(f'  [{sid}] = {{')
        for k in sorted(maps[sid].keys()):
            lines.append(f'    [{k}] = {maps[sid][k]},')
        lines.append('  },')
    lines.append('}')
    out.write_text('\n'.join(lines) + '\n', encoding='utf-8')

    builtin = ['local function loadBuiltinAttachmentMaps()',
               '    if _G.__builtinAttachLoaded then return end',
               '    mergeAttachmentMapFile({']
    for sid in sorted(maps.keys()):
        if not maps[sid]:
            continue
        builtin.append(f'        [{sid}] = {{')
        for k in sorted(maps[sid].keys()):
            builtin.append(f'            [{k}] = {maps[sid][k]},')
        builtin.append('        },')
    builtin.extend([
        '    })',
        '    _G.__builtinAttachLoaded = true',
        '    if next(_G.SkinAttachmentBaseOverrides) then',
        '        _G.__attachMapsLoaded = true',
        '    end',
        'end',
    ])

    skin_cpp = Path(__file__).resolve().parents[1] / 'app' / 'src' / 'main' / 'jni' / 'skin.cpp'
    text = skin_cpp.read_text(encoding='utf-8')
    begin = '-- ATTACH_BUILTIN_BEGIN'
    end = '-- ATTACH_BUILTIN_END'
    i0 = text.index(begin) + len(begin)
    i1 = text.index(end)
    new_text = text[:i0] + '\n' + '\n'.join(builtin) + '\n' + text[i1:]
    skin_cpp.write_text(new_text, encoding='utf-8')
    print(out, len(maps), 'skins, embedded into skin.cpp')

if __name__ == '__main__':
    main()
